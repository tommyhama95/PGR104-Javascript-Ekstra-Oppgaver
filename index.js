// Dette er en console.log(), den skriver ut i konsoll vinduet i chrome hva enn det er vi har skrevet
// inne i (). Det kan være hardkodet som eksempelet under eller en variabel som har fått en verdi.
console.log("Min første kode")

// Denne variabelen som vi har kalt 'message' har en lagret verdi som er "Hello World!" i tekst verdi / String.
var message = "Hello World!";
// Om vi kjører ut message inne i en console.log() vil det komme ut på nettsiden "Hello World!".
